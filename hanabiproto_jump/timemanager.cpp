#include "timemanager.h"

static TimeManager tm;
//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitTimeManager(void)
{
	tm.time = 0.0f;
	tm.frame = 0.0f;
	tm.nowTime = 0.0f;

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitTimeManager(void)
{

}

//=============================================================================			
// 更新処理			
//=============================================================================	
void UpdateTimeManager()
{
	//シーン遷移の条件

	//制限時間がきたら
	if (tm.time >= TIME_LIMIT)
	{
		
		//if (/*ボルテージが敵のスコアを上回っていたら。もしくはボスと雑魚敵を全て倒したら。*/)
		//{
		//	SetScene(SCENE_RESULT);
		//}
	}

	if (tm.frame = 60.0f)
	{
		tm.time++;
		tm.frame = 0.0f;
	}

	tm.frame++;
}

